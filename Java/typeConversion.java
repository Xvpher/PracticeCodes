import java.util.*;

public class typeConversion
{
public static void main (String[] args)
{
float x = 10.12f;
int a = (int)x;
long b = (long)x;
double c = x;
System.out.println("The number in float is - "+x);
System.out.println("The number in int is - "+a);
System.out.println("The number in long is - "+b);
System.out.println("The number in double is - "+c);
}
}
